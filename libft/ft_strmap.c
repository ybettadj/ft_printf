#include "libft.h"

char	*ft_strmap(char const *s, char (*f)(char))
{
	int		i;
	char	*tab;

	if (!s)
		return (NULL);
	i = ft_strlen((char*)s);
	tab = ft_strnew(i);
	if (!(tab))
		return (NULL);
	i = 0;
	while (s[i])
	{
		tab[i] = f(s[i]);
		i++;
	}
	tab[i] = '\0';
	return (tab);
}
