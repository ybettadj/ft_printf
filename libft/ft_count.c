#include "libft.h"

int		ft_count(char *str, char c)
{
	int i;
	int nb;

	i = 0;
	nb = 0;
	while (str[i] != '\0')
	{
		while (str[i] == c)
			i++;
		if (str[i] != c && str[i] != '\0')
			nb++;
		while (str[i] != c && str[i] != '\0')
			i++;
	}
	return (nb);
}
